fn main() {
    let sum = 3 + 2;
    let sub = 2 - 3;
    let prod = 6 * 7;
    let int_div = 3 / 2; // 1
    let float_div = 3.0 / 2.0; // 1.5
    let module = 37 % 2;

    println!(
        "sum={0}, sub={1}, prod={2}, int_div={3}, float_div={4}, module={5}",
        sum,
        sub,
        prod,
        int_div,
        float_div,
        module,
    );
}
